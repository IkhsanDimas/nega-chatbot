import { useState, useEffect } from 'react';
import { useNavigate } from 'react-router-dom';
import { useAuth } from '@/contexts/AuthContext';
import { supabase } from '@/integrations/supabase/client';
import { Button } from '@/components/ui/button';
import { Input } from '@/components/ui/input';
import { Label } from '@/components/ui/label';
import { useToast } from '@/hooks/use-toast';
import { Bot, Mail, Key, ArrowLeft, Loader2 } from 'lucide-react';
import { z } from 'zod';

const emailSchema = z.string().email('Email tidak valid');

const Auth = () => {
  const navigate = useNavigate();
  const { user, loading } = useAuth();
  const { toast } = useToast();
  
  const [step, setStep] = useState<'email' | 'otp'>('email');
  const [email, setEmail] = useState('');
  const [otp, setOtp] = useState('');
  const [isLoading, setIsLoading] = useState(false);
  const [countdown, setCountdown] = useState(0);

  useEffect(() => {
    if (user && !loading) {
      navigate('/chat');
    }
  }, [user, loading, navigate]);

  useEffect(() => {
    if (countdown > 0) {
      const timer = setTimeout(() => setCountdown(countdown - 1), 1000);
      return () => clearTimeout(timer);
    }
  }, [countdown]);

  const handleSendOtp = async () => {
    try {
      emailSchema.parse(email);
    } catch {
      toast({
        title: 'Email tidak valid',
        description: 'Silakan masukkan alamat email yang benar.',
        variant: 'destructive',
      });
      return;
    }

    setIsLoading(true);
    try {
      // PERBAIKAN: Menghapus emailRedirectTo agar Supabase mengirim KODE OTP (angka), bukan Magic Link.
      const { error } = await supabase.auth.signInWithOtp({
        email: email,
        options: {
          shouldCreateUser: true,
        },
      });

      if (error) throw error;

      setStep('otp');
      setCountdown(60);
      toast({
        title: 'OTP Terkirim!',
        description: 'Silakan cek email Anda untuk kode verifikasi 6 digit.',
      });
    } catch (error: any) {
      console.error('OTP error:', error);
      toast({
        title: 'Gagal mengirim OTP',
        description: error.message || 'Terjadi kesalahan. Coba lagi.',
        variant: 'destructive',
      });
    } finally {
      setIsLoading(false);
    }
  };

  const handleGoogleLogin = async () => {
    setIsLoading(true);
    try {
      const { error } = await supabase.auth.signInWithOAuth({
        provider: 'google',
        options: {
          redirectTo: `${window.location.origin}/chat`,
        },
      });

      if (error) throw error;

      toast({
        title: 'Mengarahkan ke Google...',
        description: 'Anda akan diarahkan ke halaman login Google.',
      });
    } catch (error: any) {
      console.error('Google login error:', error);
      toast({
        title: 'Gagal login dengan Google',
        description: error.message || 'Terjadi kesalahan. Coba lagi.',
        variant: 'destructive',
      });
      setIsLoading(false);
    }
  };

  const handleVerifyOtp = async () => {
    if (otp.length !== 6) {
      toast({
        title: 'Kode OTP tidak valid',
        description: 'Masukkan 6 digit kode OTP.',
        variant: 'destructive',
      });
      return;
    }

    setIsLoading(true);
    try {
      const { error } = await supabase.auth.verifyOtp({
        email: email,
        token: otp,
        type: 'email',
      });

      if (error) throw error;

      toast({
        title: 'Berhasil masuk!',
        description: 'Selamat datang di Nega.',
      });
      navigate('/chat');
    } catch (error: any) {
      console.error('Verify OTP error:', error);
      toast({
        title: 'Verifikasi gagal',
        description: error.message || 'Kode OTP salah atau sudah kadaluarsa.',
        variant: 'destructive',
      });
    } finally {
      setIsLoading(false);
    }
  };

  if (loading) {
    return (
      <div className="min-h-screen flex items-center justify-center bg-background">
        <div className="animate-spin w-8 h-8 border-2 border-primary border-t-transparent rounded-full" />
      </div>
    );
  }

  return (
    <div className="min-h-screen bg-background flex items-center justify-center p-4">
      <div className="fixed inset-0 pointer-events-none">
        <div className="absolute top-1/4 left-1/4 w-96 h-96 bg-primary/10 rounded-full blur-3xl" />
        <div className="absolute bottom-1/4 right-1/4 w-80 h-80 bg-accent/10 rounded-full blur-3xl" />
      </div>

      <div className="relative z-10 w-full max-w-md">
        <Button
          variant="ghost"
          className="mb-8 text-muted-foreground hover:text-foreground"
          onClick={() => navigate('/')}
        >
          <ArrowLeft className="w-4 h-4 mr-2" />
          Kembali
        </Button>

        <div className="glass-strong rounded-3xl p-8 animate-scale-in">
          <div className="flex items-center justify-center gap-3 mb-8">
            <div className="w-14 h-14 rounded-2xl gradient-primary flex items-center justify-center glow-primary">
              <Bot className="w-8 h-8 text-primary-foreground" />
            </div>
            <div>
              <h1 className="text-2xl font-bold text-foreground">Nega</h1>
              <p className="text-xs text-muted-foreground">AI Chatbot Indonesia</p>
            </div>
          </div>

          {step === 'email' ? (
            <>
              <h2 className="text-xl font-semibold text-center mb-2">Masuk ke Nega</h2>
              <p className="text-sm text-muted-foreground text-center mb-8">
                Masukkan email Anda untuk menerima kode OTP
              </p>

              <div className="space-y-4">
                <div className="space-y-2">
                  <Label htmlFor="email">Email</Label>
                  <div className="relative">
                    <Mail className="absolute left-3 top-1/2 -translate-y-1/2 w-5 h-5 text-muted-foreground" />
                    <Input
                      id="email"
                      type="email"
                      placeholder="nama@email.com"
                      value={email}
                      onChange={(e) => setEmail(e.target.value)}
                      className="pl-10 h-12 bg-input border-border focus:border-primary"
                      onKeyDown={(e) => e.key === 'Enter' && handleSendOtp()}
                    />
                  </div>
                </div>

                <Button
                  onClick={handleSendOtp}
                  disabled={isLoading || !email}
                  className="w-full h-12 gradient-primary text-primary-foreground hover:opacity-90"
                >
                  {isLoading ? (
                    <Loader2 className="w-5 h-5 animate-spin" />
                  ) : (
                    'Kirim Kode OTP'
                  )}
                </Button>

                {/* Divider */}
                <div className="relative">
                  <div className="absolute inset-0 flex items-center">
                    <span className="w-full border-t border-border" />
                  </div>
                  <div className="relative flex justify-center text-xs uppercase">
                    <span className="bg-background px-2 text-muted-foreground">Atau</span>
                  </div>
                </div>

                {/* Google Login Button */}
                <Button
                  onClick={handleGoogleLogin}
                  disabled={isLoading}
                  variant="outline"
                  className="w-full h-12 border-border hover:bg-secondary"
                >
                  {isLoading ? (
                    <Loader2 className="w-5 h-5 animate-spin mr-2" />
                  ) : (
                    <svg className="w-5 h-5 mr-2" viewBox="0 0 24 24">
                      <path fill="#4285F4" d="M22.56 12.25c0-.78-.07-1.53-.2-2.25H12v4.26h5.92c-.26 1.37-1.04 2.53-2.21 3.31v2.77h3.57c2.08-1.92 3.28-4.74 3.28-8.09z"/>
                      <path fill="#34A853" d="M12 23c2.97 0 5.46-.98 7.28-2.66l-3.57-2.77c-.98.66-2.23 1.06-3.71 1.06-2.86 0-5.29-1.93-6.16-4.53H2.18v2.84C3.99 20.53 7.7 23 12 23z"/>
                      <path fill="#FBBC05" d="M5.84 14.09c-.22-.66-.35-1.36-.35-2.09s.13-1.43.35-2.09V7.07H2.18C1.43 8.55 1 10.22 1 12s.43 3.45 1.18 4.93l2.85-2.22.81-.62z"/>
                      <path fill="#EA4335" d="M12 5.38c1.62 0 3.06.56 4.21 1.64l3.15-3.15C17.45 2.09 14.97 1 12 1 7.7 1 3.99 3.47 2.18 7.07l3.66 2.84c.87-2.6 3.3-4.53 6.16-4.53z"/>
                    </svg>
                  )}
                  Masuk dengan Google
                </Button>
              </div>
            </>
          ) : (
            <>
              <h2 className="text-xl font-semibold text-center mb-2">Verifikasi OTP</h2>
              <p className="text-sm text-muted-foreground text-center mb-8">
                Masukkan 6 digit kode yang dikirim ke<br />
                <span className="text-foreground font-medium">{email}</span>
              </p>

              <div className="space-y-4">
                <div className="space-y-2">
                  <Label htmlFor="otp">Kode OTP</Label>
                  <div className="relative">
                    <Key className="absolute left-3 top-1/2 -translate-y-1/2 w-5 h-5 text-muted-foreground" />
                    <Input
                      id="otp"
                      type="text"
                      placeholder="000000"
                      value={otp}
                      onChange={(e) => setOtp(e.target.value.replace(/\D/g, '').slice(0, 6))}
                      className="pl-10 h-12 bg-input border-border focus:border-primary text-center text-2xl tracking-widest"
                      maxLength={6}
                      onKeyDown={(e) => e.key === 'Enter' && handleVerifyOtp()}
                    />
                  </div>
                </div>

                <Button
                  onClick={handleVerifyOtp}
                  disabled={isLoading || otp.length !== 6}
                  className="w-full h-12 gradient-primary text-primary-foreground hover:opacity-90"
                >
                  {isLoading ? (
                    <Loader2 className="w-5 h-5 animate-spin" />
                  ) : (
                    'Verifikasi'
                  )}
                </Button>

                <div className="text-center">
                  <Button
                    variant="ghost"
                    onClick={() => {
                      if (countdown === 0) {
                        handleSendOtp();
                      }
                    }}
                    disabled={countdown > 0}
                    className="text-sm text-muted-foreground hover:text-foreground"
                  >
                    {countdown > 0 ? (
                      `Kirim ulang dalam ${countdown}s`
                    ) : (
                      'Kirim ulang kode'
                    )}
                  </Button>
                </div>

                <Button
                  variant="ghost"
                  onClick={() => setStep('email')}
                  className="w-full text-muted-foreground"
                >
                  Ganti email
                </Button>
              </div>
            </>
          )}
        </div>

        <p className="text-center text-xs text-muted-foreground mt-6">
          Hanya untuk pengguna di Indonesia
        </p>
      </div>
    </div>
  );
};

export default Auth;